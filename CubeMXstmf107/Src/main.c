/**
  * @brief System Clock Configuration
  * @retval None
	*check the peripherals RCC
	*refer to STM32UserManual
CFGR: 
SW: 02- PLL select as system clk
SWS: status of SW, should be same value as SW
HPRE: AHB prescaler, 00 - system clk not divided, therefore HCLK equals SYSCLK
PPRE1: 04 - low-speed APB1, HCLK is divided by 2 for APB1
PPRE2: 00 - high-speed APB2, HCLK not divided, therefor APB2 equals HCLK
ADCPRE: 00 - PCLK2 divided by 2
PLLSRC: 1 - Clock from PREDIV1 selected as PLL input clock, otherwise, HSI/2 selected
PLLXTPRE: 0 - PREDIV1 not divided its input clk, otherwise, divided by 2
PLLMUL: 07 - PLL input clock multiple 9 (note: 00 - *2, 01- *3)
OTGFSPRE: 00 - PLL VCO (2 * PLLCLK) clock is divided by 3 (PLL must be configured to output 72 MHz)
MCO: 00-no MCU clk output

CFGR2:
PREDIV1[3:0]: PREDIV1 division factor, 04-PREDIV1 input clock divided by 5
PREDIV2[3:0]: PREDIV2 division factor, 04 - PREDIV2 input clock divided by 5
PLL2MUL[3:0]: PLL2 Multiplication Factor, 06-PLL2 clock entry x 8
PLL3MUL[3:0]: PLL3 Multiplication Factor, 00- reserved
PREDIV1SRC: PREDIV1 entry clock source, 1 -PLL2 selected as PREDIV1 clock entry

RESULT:
HSE 25M 
PREDIV2 = 5, PLL2 multi factor = 8, then PLL2CLK = 25 / 5 * 8 = 40M
PLL = PLL2CLK / PREDIV1 = 40 / 5 = 8M
PLLCLK = PLL * PLLMUL = 8 * 9 = 72M
SYSCLK = PLLCLK = 72M
USBCLK = 72 * 2 / 3 = 48M
HCLK = SYSCLK / HPRE = 72M
APB1 = HCLK / PPRE1 = 72 / 2 = 36M
APB2 = HCLK / PPRE2 = 72 / 1 = 72M
ADCCLK = APB2 / ADCPRE = 72 / 2 = 36M
DMACLK = HCLK = 72M

HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000) define the systick clk as 72M/1000

*/
void SystemClock_Config(void)
{

  RCC_OscInitTypeDef RCC_OscInitStruct;
  RCC_ClkInitTypeDef RCC_ClkInitStruct;

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSE;
  RCC_OscInitStruct.HSEState = RCC_HSE_ON;
  RCC_OscInitStruct.HSEPredivValue = RCC_HSE_PREDIV_DIV5;
  RCC_OscInitStruct.HSIState = RCC_HSI_ON;
  RCC_OscInitStruct.Prediv1Source = RCC_PREDIV1_SOURCE_PLL2;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
  RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSE;
  RCC_OscInitStruct.PLL.PLLMUL = RCC_PLL_MUL9;
  RCC_OscInitStruct.PLL2.PLL2State = RCC_PLL2_ON;
  RCC_OscInitStruct.PLL2.PLL2MUL = RCC_PLL2_MUL8;
  RCC_OscInitStruct.PLL2.HSEPrediv2Value = RCC_HSE_PREDIV2_DIV5;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    _Error_Handler(__FILE__, __LINE__);
  }

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV2;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV1;

  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_2) != HAL_OK)
  {
    _Error_Handler(__FILE__, __LINE__);
  }

    /**Configure the Systick interrupt time 
    */
  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

    /**Configure the Systick 
    */
  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

    /**Configure the Systick interrupt time 
    */
  __HAL_RCC_PLLI2S_ENABLE();

  /* SysTick_IRQn interrupt configuration */
  HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}

